num1 = int(input("Enter first number = "))
num2 = int(input("Enter second number = "))
op = input("Enter operater for perform = ")
if op == '*':
    if num1 == 45 and num2 == 3:
        print("555")
    else:
        print(num1*num2)
elif op == '+':
    if num1 == 56 and num2 == 9:
        print("77")
    else:
        print(num1+num2)
elif op == '/':
    if num1 == 56 and num2 == 6:
        print(" 4")
    else:
        print(num1/num2)
else:
    print("Enter valid entry")